#include <iostream> 

using namespace std; 

int main() 
{ 
    int a=100;

    if(a<100)
    {
        cout<<"a is less than 100"<<endl;
    }
    else if(a>100)
    {
        cout<<"a is greater than 100"<<endl;
    }
    else if(a==100)
    {
        cout<<"a is equal to 100"<<endl;
    }
    return 0;
}

